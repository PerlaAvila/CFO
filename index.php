            <div class="col-sm-4 space">
                <p class="extra-light"><strong>1.- Aviso de Privacidad IMEF</strong></p>
                <p class="extra-light">Apegándose a la Ley Federal de Protección de Datos Personales en Posesión de Particulares (LFPDPPP, o la “Ley”), el Instituto Mexicano de Ejecutivos de Finanzas, A.C. (IMEF), con domicilio en Patricio Sanz 1516, Colonia Del Valle, México, D.F., hace de su conocimiento la siguiente información, solicitando la lectura detenida de la misma:</p>
                <p class="extra-light">El IMEF está comprometido con la transparencia, seguridad y privacidad de los Datos Personales de sus asociados. Al recopilar y tratar sus Datos Personales, IMEF, se compromete y obliga a observar y cumplir con los principios de licitud, consentimiento, calidad , información, proporcionalidad y responsabilidad previstos en el artículo 6° de la Ley.</p>
                <p class="extra-light">De acuerdo a lo anterior, el presente Aviso de Privacidad, se aplica a toda la información, incluyendo los Datos Personales Sensibles de los asociados, así como de terceros con los que IMEF pretenda iniciar una relación contractual, recopilados por IMEF para la prestación de sus servicios. Por lo antes mencionado, y al momento de leer el presente Aviso de Privacidad, usted otorga su consentimiento a IMEF, para recopilar, utilizar y transferir sus Datos Personales para los fines que más adelante se especifican.</p>
                <p class="extra-light">Con el objeto de dar cumplimiento a lo establecido en el Artículo 16 de la Ley se señala lo siguiente:
                <p class="extra-light"><strong>IDENTIDAD Y DOMICILIO DE IMEF<br />
                </strong><br />
                IMEF, es una organización dedicada a la actualización y desarrollo profesional y humano de sus asociados, a través del intercambio de ideas, experiencias y de la investigación en el campo de finanzas. Ubicado en Patricio Sanz No.1516, en la Colonia del Valle, Delegación Benito Juárez, México, D.F, C.P. 03100. Toda comunicación deberá dirigirse a Atención Gerencia de Servicio a Socios, el cual ha sido designado como responsable para el trámite de solicitudes y fomento a la protección de Datos Personales, en términos del art.30 de la Ley.<br />
                • En la recolección de datos personales se seguirán todos los principios que señala el Art. 6 de la Ley, que son: licitud, calidad, consentimiento, información, finalidad, lealtad, proporcionalidad y responsabilidad.<br />
                • Este aviso tiene aplicación a todas las actividades que realiza IMEF con terceras personas, en cualquiera de los grupos que lo conforman actualmente o que lleguen a conformarlo en el futuro, en cualquiera de las ciudades donde estos llevan a cabo sus actividades.</p>
                <p class="extra-light"><strong>2. Fuentes de Datos Personales</strong></p>
                <p class="extra-light">De acuerdo con el artículo 3° de la Ley, se entiende como ¨Datos Personales¨ cualquier información concerniente a una persona física identificada o identificable. Para efectos del presente Aviso de Privacidad, de manera enunciativa más no limitativa, se entenderán como ¨Datos Personales¨.<br />
                a) Datos Generales. Nombre(s) y apellidos, fecha de nacimiento, sexo, estado civil, existencia, o no de hijos, domicilio, teléfono fijo (casa u oficina), teléfono celular, correo electrónico, entre otros datos similares concernientes a una persona.<br />
                Para la realización de sus actividades, IMEF utiliza la siguiente información:<br />
                </p>
            </div><!-- col -->   
            <div class="col-sm-4">
                <p class="extra-light">• La allegada a IMEF directamente por diversos medios, como pueden ser entrevistas personales, informes impresos, llamadas telefónicas, correos electrónicos y otros.<br />
                • La que se obtiene cuando alguien visita los sitios de internet o de promoción de IMEF, o cuando recibe sus servicios o consulta información de IMEF.<br />
                • La que se obtiene de fuentes públicas legalmente disponibles.<br />
                Lo anterior conforme a lo establecido en el artículo 7° de la Ley.</p>
                <p class="extra-light"><strong>3. Usos de los Datos Personales</strong></p>
                <p class="extra-light">De acuerdo con lo establecido en la Ley, se considera tratamiento de Datos Personales, la obtención, uso, divulgación o almacenamiento de Datos Personales, por cualquier medio .El uso abarca cualquier acción de acceso, manejo, aprovechamiento, transferencia o disposición de datos personales.<br />
                El tratamiento de Datos Personales por parte del IMEF, se limitara al cumplimiento de las finalidades de tratamiento previstas en el presente Aviso de Privacidad y se sujetara a lo siguiente:<br />
                a) En caso que se pretenda tratar sus Datos Personales, para un fin distinto a los fines del presente Aviso de Privacidad, IMEF requerirá obtener su consentimiento para tal fin.<br />
                b) Será el que resulte necesario, adecuado y relevante para los fines descritos en este Aviso de Privacidad.<br />
                • IMEF utiliza los Datos Personales para la realización de sus actividades, como son la difusión de información y cultura financiera, la organización de seminarios y eventos técnicos y sociales, la publicación de libros y revistas técnicas, la impartición de conferencias, así como actividades administrativas, laborales y contractuales. También, con fines estadísticos y de promoción de sus eventos, de difusión de los resultados de los mismos, de información a sus asociados y patrocinadores, etc.<br />
                • En ningún caso, IMEF dará un uso distinto a los señalados a cualquier Dato Personal recabado, salvo que medie un cambio en este Aviso de Privacidad.</p>
                <p class="extra-light"><strong>4. Compartir Información</strong></p>
                <p class="extra-light">• Por regla general, IMEF no comparte información de Datos Personales sin el consentimiento explícito del titular. No obstante, es posible que IMEF comparta información en los siguientes casos, de acuerdo con lo contemplado en el capítulo V, artículos 36 y 37 de la Ley:<br />
                • Para complementar un procedimiento solicitado por el titular de la información.<br />
                • Con otros asociados de IMEF, para reforzar las relaciones entre ellos<br />
                • Con patrocinadores de eventos, a fin de que éstos tengan oportunidad de establecer contacto con los titulares.<br />
                • Para atender requerimientos de las autoridades o para proteger y defender los derechos de IMEF esto sin perjuicio del particular que proporciona información.</p>
